# Simple assignment
x = 2
y = 3
z = x + y  # z is 5

# Addition assignment
val = 6
val += 5  # (val=val+5)
print(val)  # ans is 11

# Subtraction assignment
x = 2
y = 3
x -= y  # x is -1

# Multiplication assignment
x = 2
y = 3
x *= y  # x is 6

# Division assignment
x = 10
y = 3
x /= y  # x is 3.3333333333333335

# Integer division assignment
x = 10
y = 3
x //= y  # x is 3

# Modulus assignment
x = 10
y = 3
x %= y  # x is 1
